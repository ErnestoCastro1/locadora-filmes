    private void consultFilm(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultFilm
        String nome = jTextFieldTitleFilm.getText();
        DefaultTableModel tableModel = (DefaultTableModel) jTableConsultFilm.getModel();
        tableModel.setRowCount(0);
        FilmController filmController = new FilmController();
        
        try{
            ArrayList<Film> filmes = filmController.consultFilm(nome);
            filmes.forEach((Film film) -> {
                tableModel.addRow(new Object[] {film.getCodFilm(),
                                                film.getTitle(),
                                                film.getGender(),
                                                film.getSynopsis(),
                                                film.getDuration()});
                    
            });
                jTableConsultFilm.setModel(tableModel);
        }catch(ExceptionDAO e){
               Logger.getLogger(ScreenFilm.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_consultFilm

