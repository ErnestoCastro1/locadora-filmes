    private void jTableConsultFilmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConsultFilmMouseClicked
        
        if(evt.getClickCount()==2){
            Integer codFilm = (Integer) jTableConsultFilm.getModel().getValueAt(jTableConsultFilm.getSelectedRow(), 0);
            String title = (String ) jTableConsultFilm.getModel().getValueAt(jTableConsultFilm.getSelectedRow(), 1);
            String gender = (String) jTableConsultFilm.getModel().getValueAt(jTableConsultFilm.getSelectedRow(), 2);
            String synopsis = (String) jTableConsultFilm.getModel().getValueAt(jTableConsultFilm.getSelectedRow(), 3);
            Integer duration = (Integer)jTableConsultFilm.getModel().getValueAt(jTableConsultFilm.getSelectedRow(), 4);
            
            
            String tipoTela = this.telaCadastro.getClass().getSimpleName();
            if(tipoTela.equals("ScreenFilm")){
            ScreenFilm telaCadastroFilme = (ScreenFilm) this.telaCadastro;
            telaCadastroFilme.buscarFilm(codFilm, title, gender, synopsis, duration);
            telaCadastroFilme.setVisible(true);
            } else {
                ScreenItem telaCadastroItem = (ScreenItem) this.telaCadastro;
                telaCadastroItem.buscarFilme(codFilm, title);
                telaCadastroItem.setVisible(true);
            }
            this.dispose();   
        } 
        
    }//GEN-LAST:event_jTableConsultFilmMouseClicked

