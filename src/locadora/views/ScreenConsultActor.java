    private void jTableConsultActorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConsultActorMouseClicked

        if(evt.getClickCount()==2){
            Integer codActor = (Integer) jTableConsultActor.getModel().getValueAt(jTableConsultActor.getSelectedRow(), 0);
            String name = (String) jTableConsultActor.getModel().getValueAt(jTableConsultActor.getSelectedRow(), 1);
            String nationality = (String) jTableConsultActor.getModel().getValueAt(jTableConsultActor.getSelectedRow(), 2);
            
            this.screenActor.buscarActor(codActor, name, nationality);
            this.screenActor.setVisible(true);
            this.dispose();
            
        }
    }//GEN-LAST:event_jTableConsultActorMouseClicked

