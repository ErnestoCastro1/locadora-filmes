    private void consultar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultar
       
        String name = jTextFieldNameActor.getText();
        DefaultTableModel tableModel = (DefaultTableModel) jTableConsultActor.getModel();
        tableModel.setRowCount(0);
        ActorController actorController = new ActorController();
        
        try{
            ArrayList<Actor> actors = actorController.consultActor(name);
            actors.forEach((Actor actor) -> {
                tableModel.addRow(new Object[] { 
                                                actor.getCodctor(),
                                                actor.getName(),
                                                actor.getNationality()
                                            });
            });
            jTableConsultActor.setModel(tableModel);
        }catch(ExceptionDAO e){
            Logger.getLogger(Actor.class.getName()).log(Level.SEVERE, null, e);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_consultar

