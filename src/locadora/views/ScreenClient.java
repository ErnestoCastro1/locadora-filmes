    private void saveClient(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveClient
       boolean success;
       
       try{
           ClientController clientController = new ClientController();
           if(codClient == 0){
                success = clientController.cadastrarClient(jTextFieldName.getText(), jFormattedTextFiedNif.getText(), jTextFieldEmail.getText(), 
                jTextFieldAddress.getText(), jFormattedTextFieldDtBirth.getText());
           } else {
               success = clientController.updateClient(codClient, jTextFieldName.getText(), jFormattedTextFiedNif.getText(), jTextFieldEmail.getText(), 
               jTextFieldAddress.getText(), jFormattedTextFieldDtBirth.getText());
           }
           if(success){
               JOptionPane.showMessageDialog(null, "O cadastro foi realizado com sucesso!");
               this.CleanClient(evt);
           } else {
               JOptionPane.showMessageDialog(null, "Os campos não foram preenchidos corretamente.");
           }
        }catch(Exception ex){
           JOptionPane.showMessageDialog(null, "ERRO: " + ex.getMessage());
        }
    }//GEN-LAST:event_saveClient

