    private void apagarCliente(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apagarCliente

        boolean success;
        ClientController clientController = new ClientController();
       
        try {
            success = clientController.deleteClient(codClient);
            if(success){
                JOptionPane.showMessageDialog(null, "Client apagado com sucesso!");
                this.limparCliente(evt);
            } else
                JOptionPane.showMessageDialog(null, "[ERRO] ao apagar cliente, nenhum cliente selecionado.");
        } catch (ExceptionDAO ex) {
            Logger.getLogger(ScreenClient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_apagarCliente

